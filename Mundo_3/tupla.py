# Percorre os index de LANCHE e mostra o conteudo de cada index. Igual o de cima, so q mais complicado.
# 0 vai mostrar hamburger, o 1 vai mostrar coca etc..
print("EXEMPLO 2".center(15))

for index in range(len(lanche)):
    print(lanche[index])
print("=" * 15)

