lanche= ("hamburger", "coca", "pizza", "pudim")

# Mostra o conteudo da TUPLA sem index, ou seja, hamburger, coca etc..
print("EXEMPLO 1".center(15))

for i in lanche:
    print(i)
print("=" * 15)

